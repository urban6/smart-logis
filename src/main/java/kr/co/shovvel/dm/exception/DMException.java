package kr.co.shovvel.dm.exception;

import kr.co.shovvel.hcf.exception.MessageException;

public class DMException extends MessageException {
	
    private static final long serialVersionUID = -3392195539986518720L;
	
	public DMException(String key) {
        super(key);
    }
    public DMException(String key, Throwable cause) {
        super(key, cause);
    }
    public DMException(String key, Object[] args) {
        super(key, args);
    }
    public DMException(String key, Object[] args, Throwable cause) {
        super(key, args, cause);
    }
}
