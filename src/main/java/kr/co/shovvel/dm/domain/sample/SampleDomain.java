package kr.co.shovvel.dm.domain.sample;

import kr.co.shovvel.dm.domain.common.CommonCondition;
import lombok.ToString;

@ToString
public class SampleDomain extends CommonCondition{

	private String sample_id;
	private String language_cd;
	private String sample_name;
	private String sample_type;
	private String sample_type_name;
	private String use_yn;
	private String reg_user_sno;
	private String reg_datetime;

	
	private String search_sample_type;
	private String search_sample_name;
	
	public String getSample_id() {
		return sample_id;
	}

	public void setSample_id(String sample_id) {
		this.sample_id = sample_id;
	}

	public String getSample_name() {
		return sample_name;
	}

	public void setSample_name(String sample_name) {
		this.sample_name = sample_name;
	}

	public String getReg_datetime() {
		return reg_datetime;
	}

	public void setReg_datetime(String reg_datetime) {
		this.reg_datetime = reg_datetime;
	}

	public String getSample_type() {
		return sample_type;
	}

	public void setSample_type(String sample_type) {
		this.sample_type = sample_type;
	}

	public String getSample_type_name() {
		return sample_type_name;
	}

	public void setSample_type_name(String sample_type_name) {
		this.sample_type_name = sample_type_name;
	}

	public String getSearch_sample_type() {
		return search_sample_type;
	}

	public void setSearch_sample_type(String search_sample_type) {
		this.search_sample_type = search_sample_type;
	}

	public String getSearch_sample_name() {
		return search_sample_name;
	}

	public void setSearch_sample_name(String search_sample_name) {
		this.search_sample_name = search_sample_name;
	}

	public String getReg_user_sno() {
		return reg_user_sno;
	}

	public void setReg_user_sno(String reg_user_sno) {
		this.reg_user_sno = reg_user_sno;
	}

	public String getLanguage_cd() {
		return language_cd;
	}

	public void setLanguage_cd(String language_cd) {
		this.language_cd = language_cd;
	}

	public String getUse_yn() {
		return use_yn;
	}

	public void setUse_yn(String use_yn) {
		this.use_yn = use_yn;
	}
	
}
