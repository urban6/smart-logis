package kr.co.shovvel.dm.domain.management.pay;

import lombok.Data;

@Data
public class SellerInfoVO {
	private int    seller_uid;
	private String sellerKey; 			
	private String cpId; 				
	private String productId; 			
	private String deliveryId; 			
	private String deliveryReferenceKey; 
	private String serverType; 			
	private String logYn;
	
	
	public int getSeller_uid() {
		return seller_uid;
	}
	public void setSeller_uid(int seller_uid) {
		this.seller_uid = seller_uid;
	}
	public String getSellerKey() {
		return sellerKey;
	}
	public void setSellerKey(String sellerKey) {
		this.sellerKey = sellerKey;
	}
	public String getCpId() {
		return cpId;
	}
	public void setCpId(String cpId) {
		this.cpId = cpId;
	}
	public String getProductId() {
		return productId;
	}
	public void setProductId(String productId) {
		this.productId = productId;
	}
	public String getDeliveryId() {
		return deliveryId;
	}
	public void setDeliveryId(String deliveryId) {
		this.deliveryId = deliveryId;
	}
	public String getDeliveryReferenceKey() {
		return deliveryReferenceKey;
	}
	public void setDeliveryReferenceKey(String deliveryReferenceKey) {
		this.deliveryReferenceKey = deliveryReferenceKey;
	}
	public String getServerType() {
		return serverType;
	}
	public void setServerType(String serverType) {
		this.serverType = serverType;
	}
	public String getLogYn() {
		return logYn;
	}
	public void setLogYn(String logYn) {
		this.logYn = logYn;
	} 				
	
	
}
