package kr.co.shovvel.dm.domain.main;

import java.util.List;

import javax.xml.bind.annotation.XmlRootElement;

import kr.co.shovvel.dm.domain.common.CommonCondition;

@XmlRootElement(name="DashboardProduct")
public class DashboardProduct extends CommonCondition {
	private String plan_product_id;
	private String product_id;
	private String product_name;
	private String product_version;
	private String approval_deploy_datetime;
	private String description;
	
	private List<DashboardReference> dashboardReferenceList;

	private String search_plan_product;
	private String search_plan_reference;
	
	private String user_sno;
	
	public String getPlan_product_id() {
		return plan_product_id;
	}

	public void setPlan_product_id(String plan_product_id) {
		this.plan_product_id = plan_product_id;
	}

	public String getProduct_id() {
		return product_id;
	}

	public void setProduct_id(String product_id) {
		this.product_id = product_id;
	}

	public String getProduct_name() {
		return product_name;
	}

	public void setProduct_name(String product_name) {
		this.product_name = product_name;
	}

	public String getProduct_version() {
		return product_version;
	}

	public void setProduct_version(String product_version) {
		this.product_version = product_version;
	}

	public String getApproval_deploy_datetime() {
		return approval_deploy_datetime;
	}

	public void setApproval_deploy_datetime(String approval_deploy_datetime) {
		this.approval_deploy_datetime = approval_deploy_datetime;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public List<DashboardReference> getDashboardReferenceList() {
		return dashboardReferenceList;
	}

	public void setDashboardReferenceList(List<DashboardReference> dashboardReferenceList) {
		this.dashboardReferenceList = dashboardReferenceList;
	}

	public String getSearch_plan_product() {
		return search_plan_product;
	}

	public void setSearch_plan_product(String search_plan_product) {
		this.search_plan_product = search_plan_product;
	}

	public String getSearch_plan_reference() {
		return search_plan_reference;
	}

	public void setSearch_plan_reference(String search_plan_reference) {
		this.search_plan_reference = search_plan_reference;
	}

	public String getUser_sno() {
		return user_sno;
	}

	public void setUser_sno(String user_sno) {
		this.user_sno = user_sno;
	}

	
}
